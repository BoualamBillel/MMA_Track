// Fonction utilitaire pour extraire l'organisation du titre
export const getOrganizationFromTitle = (title) => {
  const upperTitle = title.toUpperCase();
  if (upperTitle.includes("UFC")) return "UFC";
  if (upperTitle.includes("BELLATOR")) return "Bellator";
  if (upperTitle.includes("PFL")) return "PFL";
  if (upperTitle.includes("ONE")) return "ONE";
  if (upperTitle.includes("CAGE WARRIORS")) return "Cage Warriors";
  return "Autre";
};